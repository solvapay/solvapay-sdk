//! Emit Ruby signature-parity tests from canonical catalog IR.

use std::fmt::Write as _;

use crate::error::GenResult;
use crate::ir::{Ir, IrBindingCatalogLink, IrRubyReceiver};

const HEADER: &str = "# @generated by dto-gen — do not edit. Regenerate with --rb-parity-out.\n\n";

/// Emits `test/signature_parity_generated_test.rb`.
///
/// # Errors
///
/// Returns formatting failures as [`crate::error::GenError`].
pub fn emit_parity_suite_rb(ir: &Ir) -> GenResult<String> {
    let mut output = String::from(HEADER);
    output.push_str(
        "# frozen_string_literal: true\n\n\
         require \"json\"\n\
         require \"minitest/autorun\"\n\
         require \"solvapay\"\n\n\
         class SignatureParityGeneratedTest < Minitest::Test\n",
    );

    output.push_str("  OPERATION_SIGNATURES = {\n");
    let mut operations: Vec<_> = ir
        .entry_points
        .values()
        .filter(|entry| entry.ruby_target.receiver == IrRubyReceiver::ClientInstance)
        .collect();
    operations.sort_by(|left, right| left.ruby_target.name.cmp(&right.ruby_target.name));
    for entry in &operations {
        let params = entry
            .params
            .iter()
            .map(|param| {
                let kind = if param.required { "keyreq" } else { "key" };
                format!("[:{kind}, :{}]", param.names.rb)
            })
            .collect::<Vec<_>>()
            .join(", ");
        let _ = writeln!(
            output,
            "    \"{}\" => [{}],",
            entry.ruby_target.name, params
        );
    }
    output.push_str("  }.freeze\n\n");

    let mut helpers = Vec::new();
    for binding in ir.binding_symbols.values() {
        let id = match &binding.catalog {
            IrBindingCatalogLink::TopLevel(id) | IrBindingCatalogLink::CoreHelper(id) => id,
            _ => continue,
        };
        if matches!(
            id.as_str(),
            "verifyWebhook" | "withRetry" | "SolvaPayError" | "PaywallError"
        ) {
            continue;
        }
        if let Some(entry) = ir.entry_points.get(id) {
            if entry.ruby_target.receiver == IrRubyReceiver::ModuleFunction {
                helpers.push(entry.ruby_target.name.clone());
            }
        }
    }
    helpers.sort();
    helpers.dedup();
    output.push_str("  GENERATED_HELPERS = %w[\n");
    for helper in helpers {
        let _ = writeln!(output, "    {helper}");
    }
    output.push_str("  ].freeze\n\n");

    let defaults = ir
        .entry_points
        .values()
        .next()
        .map(|entry| entry.defaults.clone())
        .unwrap_or_default();
    let _ = writeln!(
        output,
        "  EXPECTED_LIMITS_CACHE_TTL_MS = {}",
        defaults.limits_cache_ttl_ms
    );
    let _ = writeln!(output, "  EXPECTED_MAX_RETRIES = {}", defaults.max_retries);
    let _ = writeln!(
        output,
        "  EXPECTED_INITIAL_DELAY_MS = {}\n",
        defaults.initial_delay_ms
    );

    output.push_str(
        "  def test_all_36_client_operations_have_exact_keyword_signatures\n\
         \x20   assert_equal 36, OPERATION_SIGNATURES.length\n\
         \x20   OPERATION_SIGNATURES.each do |name, expected|\n\
         \x20     method = SolvaPay::Client.instance_method(name)\n\
         \x20     assert_equal expected, method.parameters, name\n\
         \x20     refute SolvaPay::Client.method_defined?(\"#{name}_blocking\"), name\n\
         \x20     refute SolvaPay::Client.method_defined?(\"#{name}_async\"), name\n\
         \x20   end\n\
         \x20 end\n\n\
         \x20 def test_catalogued_generated_helpers_are_module_functions\n\
         \x20   GENERATED_HELPERS.each { |name| assert_respond_to SolvaPay, name }\n\
         \x20   assert_respond_to SolvaPay, :create\n\
         \x20   assert_instance_of SolvaPay::Payable, SolvaPay.create(api_client: Object.new).payable(product: \"prd_x\")\n\
         \x20 end\n\n\
         \x20 def test_runtime_defaults_are_exercised_through_host_bridge\n\
         \x20   assert_equal EXPECTED_LIMITS_CACHE_TTL_MS, SolvaPay::DEFAULT_LIMITS_CACHE_TTL_MS\n\
         \x20   original = SolvaPay::NativeDispatch.method(:call_sync)\n\
         \x20   captured = nil\n\
         \x20   SolvaPay::NativeDispatch.define_singleton_method(:call_sync) do |_name, args|\n\
         \x20     captured = args\n\
         \x20     0\n\
         \x20   end\n\
         \x20   attempts = 0\n\
         \x20   value = SolvaPay.with_retry(sleeper: ->(_seconds) {}) do\n\
         \x20     attempts += 1\n\
         \x20     raise \"retry\" if attempts == 1\n\
         \x20     :ok\n\
         \x20   end\n\
         \x20   assert_equal :ok, value\n\
         \x20   assert_equal EXPECTED_MAX_RETRIES, captured[\"maxRetries\"]\n\
         \x20   assert_equal EXPECTED_INITIAL_DELAY_MS, captured[\"initialDelay\"]\n\
         \x20 ensure\n\
         \x20   SolvaPay::NativeDispatch.define_singleton_method(:call_sync) { |name, args| original.call(name, args) } if original\n\
         \x20 end\n\n\
         \x20 def test_public_error_reconstruction_preserves_fields\n\
         \x20   error = SolvaPay::NativeDispatch.reconstruct_error(\n\
         \x20     \"kind\" => \"Api\", \"message\" => \"bad\", \"status\" => 409, \"code\" => \"conflict\",\n\
         \x20   )\n\
         \x20   assert_instance_of SolvaPay::SolvaPayError, error\n\
         \x20   assert_equal 409, error.status\n\
         \x20   assert_equal \"conflict\", error.code\n\
         \x20   gate = { \"kind\" => \"payment_required\", \"product\" => \"prd_x\" }\n\
         \x20   paywall = SolvaPay::NativeDispatch.reconstruct_error(\n\
         \x20     \"kind\" => \"Paywall\", \"message\" => \"blocked\", \"gate\" => gate,\n\
         \x20   )\n\
         \x20   assert_equal gate, paywall.structured_content\n\
         \x20 end\n\
         end\n",
    );
    Ok(output)
}

#[cfg(test)]
#[allow(clippy::unwrap_used, clippy::expect_used, clippy::panic)]
mod tests {
    use super::*;
    use std::collections::BTreeMap;

    #[test]
    fn emits_real_default_and_no_blocking_twins() {
        let ir = Ir {
            types: BTreeMap::new(),
            overlay_helpers: BTreeMap::new(),
            overlays: BTreeMap::new(),
            routes: vec![],
            error_templates: crate::ir::IrErrorTemplates::default(),
            entry_points: BTreeMap::new(),
            binding_symbols: BTreeMap::new(),
        };
        let output = emit_parity_suite_rb(&ir).unwrap();
        assert!(output.contains("DEFAULT_LIMITS_CACHE_TTL_MS"));
        assert!(output.contains("_blocking"));
        assert!(!output.contains("or true"));
        assert!(!output.contains("2 == 2"));
    }
}
